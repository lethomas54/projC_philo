#include "../../includes/libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	src_len;

	src_len = ft_strlen (src);
	if (!size)
		return (src_len);
	while (--size && *src)
		*(dst++) = *(src++);
	*dst = 0;
	return (src_len);
}
