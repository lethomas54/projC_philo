#include "../../includes/libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char *)s)[i] == (unsigned char)c)
			return ((void *)(&((unsigned char *)s)[i]));
		i++;
	}
	if (!(unsigned char)c && i < n)
		return ((void *)(&((unsigned char *)s)[i]));
	else
		return (NULL);
}
